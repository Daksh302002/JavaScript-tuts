//and or operator

let firstName = "Harshit";
let age = 22;

//AND
if (firstName[0] == "H" && age >= 18) {
  console.log("your name starts with H and your age is above 18");
} else {
  console.log("your name starts does not with h and your age is not above 18");
}


//OR
if (firstName[0] == "H" || age >= 18) {
  console.log("your name starts with H and your age is above 18");
} else {
  console.log("your name starts does not with h and your age is not above 18");
}
